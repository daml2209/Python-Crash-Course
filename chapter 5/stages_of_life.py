# stages of life.

age = 19
if age < 2:
    print("You are a baby! sleep well.")
elif (age>=2) and (age<4):
    print("A taddler is what you are .")
elif (age>=4) and (age<13):
    print("Hey Kido, yes you are a kid.")
elif (age>=13) and (age<20):
    print("You are now a teenager, read more.")
elif (age>=20) and (age<65):
    print("You are an adult, the journey has just begun.")
else:
    print("You are an elder, you made it.")

